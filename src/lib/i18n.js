// ============================================
// Locale-aware formatting
// ============================================
const LOCALES = { en: 'en-US', de: 'de-DE', fr: 'fr-FR', es: 'es-ES', pt: 'pt-PT' };

export function localeFor(lang) {
  return LOCALES[lang] || 'en-US';
}

// count === 1 \u2192 one, else other. Sufficient for en/de plural selection here.
export function plural(count, forms) {
  return count === 1 ? forms.one : forms.other;
}

export function formatNumber(value, lang = 'en', options) {
  if (value == null || value === '' || Number.isNaN(Number(value))) return value;
  return Number(value).toLocaleString(localeFor(lang), options);
}

export function formatDate(value, lang = 'en', options = { year: 'numeric', month: 'short', day: 'numeric' }) {
  if (!value) return value;
  const date = value instanceof Date ? value : new Date(value);
  if (Number.isNaN(date.getTime())) return value;
  return date.toLocaleDateString(localeFor(lang), options);
}

