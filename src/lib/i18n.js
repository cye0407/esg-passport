// True only in Vite dev/test. Guarded so importing this file in non-Vite
// contexts (e.g. a plain Node script) can't throw on `import.meta`.
const IS_DEV = (() => {
  try { return Boolean(import.meta.env && import.meta.env.DEV); } catch { return false; }
})();

// Replace {token} placeholders from a vars object. Unknown tokens are left as-is.
function interpolate(str, vars) {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (match, name) => (vars[name] != null ? String(vars[name]) : match));
}

/**
 * Get a translated string by key, with optional {token} interpolation.
 *   t('data.subtitle', 'de', { mode: 'Monatliche', year: 2025 })
 *
 * In DEV, a key missing from the ACTIVE language renders as `[[key]]` so
 * translation gaps are visible during QA instead of hiding behind the English
 * fallback. In production it falls back to English, then to the key itself.
 */
export function t(key, lang = 'en', vars) {
  const active = translations[lang];
  let str = active ? active[key] : undefined;
  if (str == null) {
    if (IS_DEV) return `[[${key}]]`;
    str = translations.en?.[key];
    if (str == null) return key;
  }
  return interpolate(str, vars);
}

