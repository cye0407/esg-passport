// Country VALUE is an ISO region code; the localized name comes from Intl.DisplayNames
// (falls back to the provided English name for non-ISO codes or unsupported environments).
export function localizeCountry(code, fallbackName, lang = 'en') {
  try {
    return new Intl.DisplayNames([lang], { type: 'region' }).of(code) || fallbackName;
  } catch {
    return fallbackName;
  }
}

